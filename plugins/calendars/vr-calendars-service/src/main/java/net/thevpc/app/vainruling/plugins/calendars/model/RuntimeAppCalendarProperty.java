package net.thevpc.app.vainruling.plugins.calendars.model;

/**
 *
 * @author vpc
 */
public class RuntimeAppCalendarProperty {

    private String propertyName;
    private String propertyValue;

    public RuntimeAppCalendarProperty() {
    }

    public RuntimeAppCalendarProperty(String propertyName, String propertyValue) {
        this.propertyName = propertyName;
        this.propertyValue = propertyValue;
    }

    public String getPropertyName() {
        return propertyName;
    }

    public String getPropertyValue() {
        return propertyValue;
    }

    public void setPropertyName(String propertyName) {
        this.propertyName = propertyName;
    }

    public void setPropertyValue(String propertyValue) {
        this.propertyValue = propertyValue;
    }

}
