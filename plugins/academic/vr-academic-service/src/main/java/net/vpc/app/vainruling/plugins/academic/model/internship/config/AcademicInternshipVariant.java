package net.vpc.app.vainruling.plugins.academic.model.internship.config;

import net.vpc.upa.RelationshipType;
import net.vpc.upa.config.*;

/**
 * @author taha.bensalah@gmail.com
 */
@Entity(listOrder = "this.name")
@Path("Repository/Education")
public class AcademicInternshipVariant {

    @Path("Main")
    @Id
    @Sequence

    private int id;
    @Main
    private String name;
    @ManyToOne(relationType = RelationshipType.COMPOSITION)
    private AcademicInternshipType internshipType;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public AcademicInternshipType getInternshipType() {
        return internshipType;
    }

    public void setInternshipType(AcademicInternshipType internshipType) {
        this.internshipType = internshipType;
    }

}
